public class UseArray {
	public static void main(String[] args) {
		int[] students = new int[5];
		students[0] = 55;
		students[1] = 66;
		students[2] = 77;
		students[3] = 88;
		students[4] = 99;

		System.out.println("The first student score is:" + students[0]);
		System.out.println("The third student score is:" + students[2]);
		System.out.println("The fifth student score is:" + students[4]);
	}
}